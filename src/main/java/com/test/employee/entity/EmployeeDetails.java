package com.test.employee.entity;

public class EmployeeDetails {
	
	private String employeeId;
	
	private String firstName;
	
	private String lastName;
	
	private String yearlySalary;
	
	private String taxAmount;
	
	private String cessAmount;

	public String getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getYearlySalary() {
		return yearlySalary;
	}

	public void setYearlySalary(String yearlySalary) {
		this.yearlySalary = yearlySalary;
	}

	public String getTaxAmount() {
		return taxAmount;
	}

	public void setTaxAmount(String taxAmount) {
		this.taxAmount = taxAmount;
	}

	public String getCessAmount() {
		return cessAmount;
	}

	public void setCessAmount(String cessAmount) {
		this.cessAmount = cessAmount;
	}
	
	
	

}
